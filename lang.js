function applyLanguage(lang) {
  document.documentElement.lang = lang === "hy" ? "hy" : "en";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.placeholder = translations[lang][key];
    }
  });

  document.querySelectorAll(".lang-switch span").forEach((el) => {
    el.textContent = translations[lang]["lang-switch"];
  });

  document.dispatchEvent(
    new CustomEvent("languagechange", { detail: { lang } }),
  );
}

function getLanguage() {
  return localStorage.getItem("site-lang") === "en" ? "en" : "hy";
}

function setLanguage(lang) {
  localStorage.setItem("site-lang", lang);
  applyLanguage(lang);
}

document.addEventListener("DOMContentLoaded", () => {
  const currentLang = getLanguage();
  applyLanguage(currentLang);

  document.querySelectorAll(".lang-switch").forEach((btn) => {
    btn.addEventListener("click", () => {
      const newLang = getLanguage() === "hy" ? "en" : "hy";
      setLanguage(newLang);
    });
  });
});
